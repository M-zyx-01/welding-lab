def get_material(key: str) -> MaterialSpec:
    key_upper = key.upper()
    if key_upper in MATERIALS:
        return MATERIALS[key_upper]
    for k, v in MATERIALS.items():
        if key.lower() in v.name.lower():
            return v
    raise KeyError(f"Material {key!r} not found. Available: {list(MATERIALS.keys())}")

def list_materials() -> list[str]:
    return list(MATERIALS.keys())
